    // ─────────────────────────────────────────────────
    public function run(): void
    {
        // Cache semua semester, matakuliah, mahasiswa
        $semesters  = Semester::all()->keyBy('code');
        $matakuliahs = Matakuliah::all()->keyBy('code');

        foreach ($this->prodiSemMap as $prodiCode => [$nrpPrefix, $semesterCodes]) {
            $prodi = ProgramStudy::where('code', $prodiCode)->first();
            if (! $prodi) continue;

            $mahasiswas = Mahasiswa::where('program_study_id', $prodi->id)
                ->orderBy('nrp')
                ->get();

            $mkPerSem = $this->mkPerSemester[$prodiCode] ?? [];

            if (empty($mahasiswas) || empty($mkPerSem)) continue;

            foreach ($mahasiswas as $idx => $mhs) {
                $nomorMhs = $idx + 1; // 1-based index

                foreach ($semesterCodes as $semIdx => $semCode) {
                    $semNo  = $semIdx + 1;          // 1-based semester number
                    $sem    = $semesters[$semCode] ?? null;
                    if (! $sem) continue;

                    // ── Nilai Akademik ──
                    $mkCodes    = $mkPerSem[$semNo] ?? [];
                    $akademikRow = $this->akademikBase[$nrpPrefix][$nomorMhs][$semIdx] ?? [75, 78, 80];

                    foreach ($mkCodes as $mkCode) {
                        $mk = $matakuliahs[$mkCode] ?? null;
                        if (! $mk) continue;

                        // Variasi kecil per matakuliah agar tidak semua sama
                        $seed = crc32($mhs->nrp . $mkCode . $semCode) % 7;
                        $nu   = max(40, min(100, $akademikRow[0] + $seed - 3));
                        $uts  = max(40, min(100, $akademikRow[1] + $seed - 2));
                        $uas  = max(40, min(100, $akademikRow[2] + $seed - 1));

                        $score = AcademicScors::updateOrCreate(
                            [
                                'mahasiswa_id'  => $mhs->id,
                                'matakuliah_id' => $mk->id,
                                'semester_id'   => $sem->id,
                            ],
                            [
                                'nu'  => $nu,
                                'uts' => $uts,
                                'uas' => $uas,
                            ]
                        );
                        // final_numeric & final_letter dihitung otomatis di model booted()
                        // tapi kita eksplisit set agar seeder independen
                        if ($score->final_numeric === null) {
                            $score->final_numeric = $score->computeFinalNumeric();
                            $score->final_letter  = $score->toLetter();
                            $score->saveQuietly();
                        }
                    }

                    // ── Nilai Kepribadian ──
                    $kepRow = $this->kepribadianBase[$nrpPrefix][$nomorMhs][$semIdx] ?? [75, 78, 76];

                    $pa = PersonalityAssessment::updateOrCreate(
                        [
                            'mahasiswa_id' => $mhs->id,
                            'semester_id'  => $sem->id,
                        ],
                        [
                            'director_score' => $kepRow[0],
                            'korsis_score'   => $kepRow[1],
                            'kaprodi_score'  => $kepRow[2],
                        ]
                    );
                    if ($pa->npk_final === null) {
                        $pa->npk_final = $pa->computeNpk();
                        $pa->saveQuietly();
                    }

                    // ── Nilai Jasmani ──
                    $jasRow = $this->jasmaniBase[$nrpPrefix][$nomorMhs][$semIdx] ?? [75, 77, 73];

                    $pt = PhysicalTest::updateOrCreate(
                        [
                            'mahasiswa_id' => $mhs->id,
                            'semester_id'  => $sem->id,
                        ],
                        [
                            'posture_score' => $jasRow[0],
                            'garjas_score'  => $jasRow[1],
                            'swim_score'    => $jasRow[2],
                        ]
                    );
                    if ($pt->naj === null) {
                        $pt->naj = $pt->computeNaj();
                        $pt->saveQuietly();
                    }

                    // ── Nilai TOEFL (Hanya di semester akhir) ──
                    if ($semIdx === array_key_last($semesterCodes)) {
                        // Seed TOEFL secara acak, bervariasi antara lulus dan tidak
                        // Misalnya tier A dan B biasanya lolos di atas 450, tier C dan D acak.
                        // Namun buat beberapa kasus tier A gagal TOEFL sbg contoh.
                        $randomScore = $akademikRow[0] > 80 ? rand(450, 580) : rand(380, 480);
                        
                        // Buat mahasiswa nomor 1 di suatu prodi (Tier A) gagal TOEFL kadang-kadang
                        // biar terlihat logikanya berjalan
                        if ($nomorMhs === 1 && $prodiCode === 'D3-TI') {
                            $randomScore = 390; // Sengaja tidak lulus D3 (syarat 400)
                        }

                        ToeflScore::updateOrCreate(
                            ['mahasiswa_id' => $mhs->id],
                            [
                                'score' => $randomScore,
                                'test_date' => now()->subDays(rand(1, 30))->format('Y-m-d'),
                            ]
                        );
                    }

                    // ── Rekap MahasiswaSemester (IPS, IPK, NPA, NPK, NPJ, NA) ──
                    $this->rekapSemester($mhs, $sem->id);
                }
            }
        }
    }

    /**
     * Hitung dan simpan rekap MahasiswaSemester untuk satu mahasiswa + semester.
     */
    private function rekapSemester(Mahasiswa $mhs, int $semesterId): void
    {
        // IPS semester ini
        $scores = $mhs->academicScores()
            ->where('semester_id', $semesterId)
            ->with('matakuliah:id,sks')
            ->get();

        $sksTot   = 0;
        $bobotTot = 0.0;
        foreach ($scores as $sc) {
            $fn  = $sc->final_numeric ?? $sc->computeFinalNumeric() ?? 0;
            $ip  = AcademicScors::ipOf($fn);
            $sks = $sc->matakuliah?->sks ?? 0;
            $sksTot   += $sks;
            $bobotTot += $sks * $ip;
        }
        $ips = $sksTot ? round($bobotTot / $sksTot, 2) : 0.0;

        // IPK kumulatif
        $ipk = $mhs->computeIpk();

        // NPA
        $npa = MahasiswaSemester::ipToNpa($ipk);

        // NPK
        $npk = optional(
            $mhs->personalityAssessments()->where('semester_id', $semesterId)->first()
        )->npk_final ?? 0.0;

        // NPJ
        $npj = optional(
            $mhs->physicalTests()->where('semester_id', $semesterId)->first()
        )->naj ?? 0.0;

        // NA
        $na = MahasiswaSemester::computeNa($npa, $npk, $npj);

        $rec = MahasiswaSemester::updateOrCreate(
            [
                'mahasiswa_id' => $mhs->id,
                'semester_id'  => $semesterId,
            ],
            [
                'ips'  => $ips,
                'ipk'  => $ipk,
                'npa'  => $npa,
                'npk'  => $npk,
                'npj'  => $npj,
                'na'   => $na,
            ]
        );
        $rec->eligible_next = $rec->computeEligibility();
        $rec->saveQuietly();
    }
